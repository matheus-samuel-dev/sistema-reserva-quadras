-- Dynamic sport modality catalog. Existing domain rows continue to store stable modality codes.
-- SQL is portable between PostgreSQL and H2 in PostgreSQL compatibility mode.

CREATE TABLE sport_modality (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(40) NOT NULL,
    name VARCHAR(255) NOT NULL,
    normalized_name VARCHAR(255) NOT NULL,
    active BOOLEAN NOT NULL,
    default_price NUMERIC(10, 2) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT uk_sport_modality_code UNIQUE (code),
    CONSTRAINT uk_sport_modality_normalized_name UNIQUE (normalized_name),
    CONSTRAINT ck_sport_modality_price CHECK (default_price >= 0)
);

