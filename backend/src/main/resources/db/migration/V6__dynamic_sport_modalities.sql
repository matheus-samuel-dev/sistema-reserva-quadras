-- Earlier versions stored free-form display labels in this element collection. First materialize
-- every historical value in the catalog, then replace labels with stable codes. This keeps custom
-- sports created before V6 and prevents the new FK from making an existing installation unbootable.
CREATE TABLE user_sports_source (
    user_id BIGINT NOT NULL,
    display_name VARCHAR(255) NOT NULL,
    normalized_name VARCHAR(255),
    known_code VARCHAR(40)
);

INSERT INTO user_sports_source (user_id, display_name)
SELECT user_id,
       REPLACE(REPLACE(REPLACE(REPLACE(REPLACE(TRIM(sport), '  ', ' '), '  ', ' '), '  ', ' '), '  ', ' '), '  ', ' ')
FROM user_sports
WHERE TRIM(sport) <> '';

UPDATE user_sports_source
SET normalized_name = LOWER(TRANSLATE(
        display_name,
        'áàâãäéèêëíìîïóòôõöúùûüçñ',
        'aaaaaeeeeiiiiooooouuuucn'
    ));

UPDATE user_sports_source
SET known_code = CASE
    WHEN normalized_name IN ('beach tennis', 'beach_tennis') THEN 'BEACH_TENNIS'
    WHEN normalized_name = 'futevolei' THEN 'FUTEVOLEI'
    WHEN normalized_name = 'society' THEN 'SOCIETY'
    WHEN normalized_name = 'tenis' THEN 'TENIS'
    WHEN normalized_name = 'volei' THEN 'VOLEI'
    WHEN normalized_name = 'basquete' THEN 'BASQUETE'
    ELSE NULL
END;

INSERT INTO sport_modality (code, name, normalized_name, active, default_price, created_at, updated_at)
SELECT 'LEGACY_' || CAST(ROW_NUMBER() OVER (ORDER BY normalized_name) AS VARCHAR(20)),
       MIN(display_name), normalized_name, TRUE, 0.00, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP
FROM user_sports_source
WHERE known_code IS NULL
GROUP BY normalized_name;

CREATE TABLE user_sports_migrated (
    user_id BIGINT NOT NULL,
    sport VARCHAR(255) NOT NULL
);

INSERT INTO user_sports_migrated (user_id, sport)
SELECT user_id, known_code
FROM user_sports_source
WHERE known_code IS NOT NULL
UNION
SELECT source.user_id, catalog.code
FROM user_sports_source source
JOIN sport_modality catalog ON catalog.normalized_name = source.normalized_name
WHERE source.known_code IS NULL;

DELETE FROM user_sports;
INSERT INTO user_sports (user_id, sport)
SELECT user_id, sport FROM user_sports_migrated;
DROP TABLE user_sports_migrated;
