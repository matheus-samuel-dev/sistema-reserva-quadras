ALTER TABLE users ADD COLUMN availability VARCHAR(1000);

CREATE TABLE user_preferences (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    theme VARCHAR(20) NOT NULL,
    notifications_enabled BOOLEAN NOT NULL,
    reservation_reminder_hours INTEGER NOT NULL,
    email_notifications BOOLEAN NOT NULL,
    browser_notifications BOOLEAN NOT NULL,
    default_city VARCHAR(120),
    favorite_modalities VARCHAR(500),
    preferred_times VARCHAR(1000),
    private_profile BOOLEAN NOT NULL,
    discoverable_by_partners BOOLEAN NOT NULL,
    language VARCHAR(10) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_user_preferences_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT uk_user_preferences_user UNIQUE (user_id),
    CONSTRAINT ck_user_preferences_reminder CHECK (reservation_reminder_hours BETWEEN 0 AND 168)
);

CREATE TABLE platform_settings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_name VARCHAR(160) NOT NULL,
    legal_name VARCHAR(160),
    document VARCHAR(30),
    company_email VARCHAR(254),
    company_phone VARCHAR(30),
    address VARCHAR(255),
    timezone VARCHAR(60) NOT NULL,
    opening_time TIME NOT NULL,
    closing_time TIME NOT NULL,
    operating_days VARCHAR(100) NOT NULL,
    cancellation_rule_hours INTEGER NOT NULL,
    minimum_reservation_minutes INTEGER NOT NULL,
    maximum_advance_days INTEGER NOT NULL,
    slot_minutes INTEGER NOT NULL,
    enabled_modalities VARCHAR(200) NOT NULL,
    beach_tennis_price NUMERIC(10, 2) NOT NULL,
    futevolei_price NUMERIC(10, 2) NOT NULL,
    society_price NUMERIC(10, 2) NOT NULL,
    tenis_price NUMERIC(10, 2) NOT NULL,
    volei_price NUMERIC(10, 2) NOT NULL,
    basquete_price NUMERIC(10, 2) NOT NULL,
    accept_pix BOOLEAN NOT NULL,
    accept_card BOOLEAN NOT NULL,
    accept_cash BOOLEAN NOT NULL,
    pix_key VARCHAR(254),
    email_notifications BOOLEAN NOT NULL,
    browser_notifications BOOLEAN NOT NULL,
    reservation_reminder_hours INTEGER NOT NULL,
    primary_color VARCHAR(20) NOT NULL,
    logo_url VARCHAR(255),
    default_theme VARCHAR(20) NOT NULL,
    minimum_password_length INTEGER NOT NULL,
    session_minutes INTEGER NOT NULL,
    require_strong_password BOOLEAN NOT NULL,
    public_registration_enabled BOOLEAN NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT ck_platform_settings_hours CHECK (opening_time < closing_time),
    CONSTRAINT ck_platform_settings_reservation CHECK (
        cancellation_rule_hours >= 0 AND minimum_reservation_minutes > 0
        AND maximum_advance_days > 0 AND slot_minutes > 0
    ),
    CONSTRAINT ck_platform_settings_prices CHECK (
        beach_tennis_price >= 0 AND futevolei_price >= 0 AND society_price >= 0
        AND tenis_price >= 0 AND volei_price >= 0 AND basquete_price >= 0
    )
);

