CREATE TABLE championship_event (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(1600) NOT NULL,
    modality VARCHAR(40) NOT NULL,
    court_id BIGINT NOT NULL,
    location VARCHAR(255) NOT NULL,
    city VARCHAR(120) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    registration_deadline DATE NOT NULL,
    max_participants INTEGER NOT NULL,
    format VARCHAR(120) NOT NULL,
    prize VARCHAR(255) NOT NULL,
    registration_fee NUMERIC(10, 2) NOT NULL,
    regulation VARCHAR(6000) NOT NULL,
    status VARCHAR(40) NOT NULL,
    image_url VARCHAR(500),
    bracket VARCHAR(6000),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_championship_event_court FOREIGN KEY (court_id) REFERENCES court (id),
    CONSTRAINT ck_championship_event_modality CHECK (modality IN ('BEACH_TENNIS', 'FUTEVOLEI', 'SOCIETY', 'TENIS', 'VOLEI', 'BASQUETE')),
    CONSTRAINT ck_championship_event_status CHECK (status IN ('RASCUNHO', 'INSCRICOES_ABERTAS', 'INSCRICOES_ENCERRADAS', 'EM_ANDAMENTO', 'CONCLUIDO', 'CANCELADO')),
    CONSTRAINT ck_championship_event_dates CHECK (start_date <= end_date AND registration_deadline <= start_date),
    CONSTRAINT ck_championship_event_capacity CHECK (max_participants > 0 AND registration_fee >= 0),
    CONSTRAINT uk_championship_event_name_date UNIQUE (name, start_date)
);

CREATE TABLE championship_enrollment (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    championship_id BIGINT NOT NULL,
    player_id BIGINT NOT NULL,
    status VARCHAR(24) NOT NULL,
    cancelled_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_championship_enrollment_event FOREIGN KEY (championship_id) REFERENCES championship_event (id) ON DELETE CASCADE,
    CONSTRAINT fk_championship_enrollment_player FOREIGN KEY (player_id) REFERENCES users (id),
    CONSTRAINT uk_championship_enrollment_player UNIQUE (championship_id, player_id),
    CONSTRAINT ck_championship_enrollment_status CHECK (status IN ('ATIVA', 'CANCELADA'))
);

CREATE TABLE sports_profile (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    city VARCHAR(120) NOT NULL,
    objective VARCHAR(40) NOT NULL,
    presentation VARCHAR(1200) NOT NULL,
    position VARCHAR(120),
    discoverable BOOLEAN NOT NULL,
    avatar_url VARCHAR(500),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_sports_profile_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    CONSTRAINT uk_sports_profile_user UNIQUE (user_id),
    CONSTRAINT ck_sports_profile_objective CHECK (objective IN ('TREINO', 'JOGO_CASUAL', 'COMPETICAO', 'ENCONTRAR_TIME'))
);

CREATE TABLE sports_profile_region (
    profile_id BIGINT NOT NULL,
    region VARCHAR(120) NOT NULL,
    CONSTRAINT pk_sports_profile_region PRIMARY KEY (profile_id, region),
    CONSTRAINT fk_sports_profile_region_profile FOREIGN KEY (profile_id) REFERENCES sports_profile (id) ON DELETE CASCADE
);

CREATE TABLE sports_profile_modality (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_id BIGINT NOT NULL,
    modality VARCHAR(40) NOT NULL,
    level VARCHAR(40) NOT NULL,
    primary_modality BOOLEAN NOT NULL,
    CONSTRAINT fk_sports_profile_modality_profile FOREIGN KEY (profile_id) REFERENCES sports_profile (id) ON DELETE CASCADE,
    CONSTRAINT uk_profile_modality UNIQUE (profile_id, modality),
    CONSTRAINT ck_sports_profile_modality CHECK (modality IN ('BEACH_TENNIS', 'FUTEVOLEI', 'SOCIETY', 'TENIS', 'VOLEI', 'BASQUETE')),
    CONSTRAINT ck_sports_profile_level CHECK (level IN ('INICIANTE', 'INTERMEDIARIO', 'AVANCADO', 'COMPETITIVO'))
);

CREATE TABLE sports_availability (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_id BIGINT NOT NULL,
    day_of_week VARCHAR(16) NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    CONSTRAINT fk_sports_availability_profile FOREIGN KEY (profile_id) REFERENCES sports_profile (id) ON DELETE CASCADE,
    CONSTRAINT ck_sports_availability_time CHECK (start_time < end_time),
    CONSTRAINT ck_sports_availability_day CHECK (day_of_week IN ('MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY', 'SUNDAY'))
);

CREATE TABLE partner_interest (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sender_id BIGINT NOT NULL,
    receiver_id BIGINT NOT NULL,
    status VARCHAR(24) NOT NULL,
    message VARCHAR(500),
    responded_at TIMESTAMP WITH TIME ZONE,
    cancelled_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT fk_partner_interest_sender FOREIGN KEY (sender_id) REFERENCES users (id),
    CONSTRAINT fk_partner_interest_receiver FOREIGN KEY (receiver_id) REFERENCES users (id),
    CONSTRAINT uk_partner_interest_pair UNIQUE (sender_id, receiver_id),
    CONSTRAINT ck_partner_interest_distinct_users CHECK (sender_id <> receiver_id),
    CONSTRAINT ck_partner_interest_status CHECK (status IN ('PENDENTE', 'ACEITO', 'RECUSADO', 'CANCELADO'))
);

CREATE INDEX idx_user_preferences_user ON user_preferences (user_id);
CREATE INDEX idx_championship_event_search ON championship_event (status, modality, city, start_date);
CREATE INDEX idx_championship_enrollment_event_status ON championship_enrollment (championship_id, status);
CREATE INDEX idx_championship_enrollment_player ON championship_enrollment (player_id, created_at);
CREATE INDEX idx_sports_profile_discovery ON sports_profile (discoverable, city, objective);
CREATE INDEX idx_sports_profile_modality_search ON sports_profile_modality (modality, level);
CREATE INDEX idx_partner_interest_sender_status ON partner_interest (sender_id, status);
CREATE INDEX idx_partner_interest_receiver_status ON partner_interest (receiver_id, status);
